package practice;

public class Case {
	
	
	public static void main(String[] args) {
		int x = 6;
		
		switch(x) {
		
		case 6:
			System.out.println("case 6 ran");
			break;
		default:
			System.out.println("The default case ran");
		case 7:
			System.out.println("case 7 ran");
		case 8:
			System.out.println("case 10 ran");
		
		}
	}

}
